jedi = ["Yoda", "Luke", "Carlos"]

# inserçao (métdodo .append)

jedi.append("Nargas")

print(jedi)

# inserção com append + input

jedi.append(input("Entre com o nome do seu jedi: "))

print(jedi)

# inserção em qualquer posição(médoto .insert) = .insert(posiçao, valor)

jedi.insert(1, "cebolinha")

print(jedi)