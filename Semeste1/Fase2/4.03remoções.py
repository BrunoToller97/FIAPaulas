jedi = ["Yoda", "Luke", "Carlos", "ronaldo", "ronaldoso", "ronaldinho"]

# remoção (métdodo .pop) // quando pop(vazio), remove o último item

jedi.pop()

# remover com posição // quando passa o index do item

jedi.pop(1)

# remover item de valor específico (método .remove) // quando passa o valor como parametro

jedi.remove("Yoda")

print(jedi)

# Limpar a lista (método .clear) // remove tudo

jedi.clear()

print(jedi)